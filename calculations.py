# create class Calculator
class Calculator:
    # define compute operations
    def add(self, num_1, num_2):
        sum = num_1 + num_2
        return sum
    
    def sub(self, num_1, num_2):
        diff = num_1 - num_2
        return diff
    
    def multiply(self, num_1, num_2):
        prod =  num_1 * num_2
        return prod
    
    def div(self, num_1, num_2):
        quo = num_1 / num_2
        return quo